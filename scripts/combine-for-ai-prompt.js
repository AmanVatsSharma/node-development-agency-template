/**
 * @fileoverview Combine main site files for AI prompt
 * @description Outputs a single file containing Home, About, Header, Footer,
 * and all their component/data/style dependencies (excluding services landing pages).
 *
 * Usage (from project root):
 *   node scripts/combine-for-ai-prompt.js
 *
 * Output: COMBINED_MAIN_SITE_FOR_AI_PROMPT.txt
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const OUT_FILE = path.join(ROOT, 'COMBINED_MAIN_SITE_FOR_AI_PROMPT.txt');
const SECTION_HEADER = (filePath) => `\n========== FILE: ${filePath} ==========\n`;

// Ordered list: styles → lib → data → hooks → ui → components → animations → home → layout shell → layout → pages
const FILES = [
  'app/globals.css',
  'app/lib/cn.ts',
  'app/lib/seo/constants.ts',
  'app/lib/seo/metadata.ts',
  'app/data/navigation.ts',
  'app/data/companyProfile.ts',
  'app/hooks/use-outside-click.ts',
  'app/components/ui/resizable-navbar.tsx',
  'app/components/ThemeProvider.tsx',
  'app/components/ThemeToggle.tsx',
  'app/components/ServiceIcon.tsx',
  'app/components/AnimatedIllustration.tsx',
  'app/components/NewsletterSignup.tsx',
  'app/components/EnhancedMobileMenu.tsx',
  'app/components/HeroAnimationWrapper.tsx',
  'app/animations/HeroAnimationLite.tsx',
  'app/animations/components/WasmParticles.tsx',
  'app/animations/HeroAnimationWorldClass.tsx',
  'app/components/home/StatsCounter.tsx',
  'app/components/home/TechStackShowcase.tsx',
  'app/components/home/IndustryShowcase.tsx',
  'app/components/home/WhyChooseUs.tsx',
  'app/components/home/CaseStudyShowcase.tsx',
  'app/components/home/TestimonialCarousel.tsx',
  'app/components/home/ProcessTimeline.tsx',
  'app/components/home/GlobalPresence.tsx',
  'app/components/EnhancedHeader.tsx',
  'app/components/Footer.tsx',
  'app/providers.tsx',
  'app/layout.tsx',
  'app/page.tsx',
  'app/pages/about/layout.tsx',
  'app/pages/about/page.tsx',
];

function main() {
  let out = '';
  const missing = [];

  out += 'COMBINED MAIN SITE (Home + About + Header + Footer + dependencies)\n';
  out += 'Excludes: services landing pages, shopify/, GoogleAdsEcosystem/, admin, login.\n';
  out += 'Generated by: node scripts/combine-for-ai-prompt.js\n';

  for (const filePath of FILES) {
    const abs = path.join(ROOT, filePath);
    if (!fs.existsSync(abs)) {
      missing.push(filePath);
      out += SECTION_HEADER(filePath);
      out += `// FILE NOT FOUND (skipped): ${filePath}\n`;
      continue;
    }
    const content = fs.readFileSync(abs, 'utf8');
    out += SECTION_HEADER(filePath);
    out += content;
    if (!content.endsWith('\n')) out += '\n';
  }

  fs.writeFileSync(OUT_FILE, out, 'utf8');

  console.log('Wrote:', OUT_FILE);
  if (missing.length) {
    console.log('Missing files:', missing.join(', '));
  }
}

main();
